function sumar(value1,value2){
    var result1 = value1+value2;
    return result1;
}
function restar(value1,value2){
    var result1 = value1-value2;
    return result1;
}
function multiplicar(value1,value2){
    var result1 = value1*value2;
    return result1;
}
function dividir(value1,value2){
    var result1 = value1/value2;
    return result1;
}